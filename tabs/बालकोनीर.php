<h1>बालकोनीर बराण्डा</h1>
<ul>
<li>बाल्कोनी घरको सौन्दर्यवर्धक  हुन्छ</li>
<li>सौर्यप्रकाश र स्वच्छहावाको लागि नि बाल्कोनीको आवश्यकता पर्दछ</li>
<li>समुचित स्वास्थ्य ब्यवस्थापनका लागि पनि बाल्कोनिको आबश्यकता पर्दछ</li>
<li>बाल्कोनी पूर्व या उत्तर भागमा नै बनाउनु पर्छ</li>
<li>बराण्डा ईशानमा राम्रो हुन्छ र त्यसका खम्बाहरु गोलहुनु राम्रो मानिन्छ</li>
</ul>